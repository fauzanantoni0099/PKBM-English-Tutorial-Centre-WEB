    {
        $permissions = Permission::all();
        if ($request->input('query'))
        {
            $query = $request->input('query');
            $roles = Role::where('name','LIKE',"%$query%")->paginate();
        }
        else
        {
            $roles = Role::latest()->paginate();
        }

        return view('backend.roles.index',compact('roles','permissions'));
    }

